CREATE FUNCTION DBO.REMOVENONNUMERICS(@VAR VARCHAR(MAX))
RETURNS VARCHAR(MAX)
AS BEGIN

	DECLARE @P INT = 1;

	DECLARE @VAR2 VARCHAR(MAX) = ''

	WHILE @P <= LEN(@VAR) BEGIN
		
		IF (ISNUMERIC(SUBSTRING(@VAR, @P, 1)) = 1) BEGIN

			SET @VAR2 = @VAR2 + SUBSTRING(@VAR, @P, 1)

		END

		SET @P += 1

	END

	RETURN @VAR2


END


SELECT DBO.REMOVENONNUMERICS('ERF: 025')